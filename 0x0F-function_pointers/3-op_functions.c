#include "3-calc.h"

/**
 * op_add - Adds two numbers
 * @a: The first number
 * @b: The second number
 * Return: The sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Substracts two numbers
 * @a: The first number
 * @b: The second number
 * Return: The dif
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies two numbers
 * @a: The first number
 * @b: The second number
 * Return: The product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides two numbers
 * @a: The first number
 * @b: The second number
 * Return: The div
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Returns the remainder of the division of two numbers
 * @a: The first number
 * @b: The second number
 * Return: Reaminder of division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
